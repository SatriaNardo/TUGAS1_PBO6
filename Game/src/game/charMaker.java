    private void healthTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_healthTextActionPerformed
        // TODO add your handling code here:
        darah = Integer.valueOf(healthText.getText());;
        if(darah > 250)
        {
            System.out.println("Health is Over the Maximum");
            darah = 250;
            healthText.setText(Integer.toString(darah));
        }
        else if(darah < 10)
        {
            System.out.println("Health is Too Low");
            darah = 10;
            healthText.setText(Integer.toString(darah));
        }
        healthBar.setValue(darah);
        
    }//GEN-LAST:event_healthTextActionPerformed

