    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        
        daftarChar = new ArrayList <charStat> ();
        charStat karakter = new charStat();
        int totalStat = tahan + serang+ darah;
        if(check > 0)
        {
           System.out.println("Overwriting...");
            try {
                Thread.sleep(2000);
            } catch (InterruptedException ex) {
              
            } 
        }
        if(totalStat < 280)
            {
                System.out.println("Karakter Ditambah");
                karakter.attack = serang;
                karakter.defense = tahan;
                karakter.health = darah;
                karakter.name = valueOf(nameText.getText());;
                daftarChar.add(karakter);
                check = check + 1;
                
            }
        else
        {
            System.out.println("Status Karakter Melebihi Batas");
        }
    }//GEN-LAST:event_createButtonActionPerformed
    
